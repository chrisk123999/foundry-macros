let tokenId = args[0];
if (!tokenId) return;
let token = await canvas.tokens.get(tokenId);
if (!token) return;
let actor = token.actor;
let effectId = args[1];
if (!effectId) return;
let effect = actor.effects.get(effectId);
if (!effect) return;
let updates = args[2];
if (!updates) return;
effect.update(updates);